import socket
kill_command = [0x18, 0x06, 0xC0, 0x00, 0x00, 0x15, 0x05, 0x35, 0x43, 0x49, 0x5F, 0x4C, 0x41, 0x42, 0x5F, 0x41, 0x50, 0x50, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00]

def transmit(command):
    byte_message = bytes(command)
    opened_socket = socket.socket(socket.AF_INET, socket.SOCK_DGRAM)
    opened_socket.sendto(byte_message, ("127.0.0.1", 1234))

transmit(kill_command)
